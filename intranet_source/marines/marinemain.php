      <td colspan="3" align="left">The National 
					Museum of the Marine Corps is being built as the result of a 
					public-private venture between the Marine Corps and the 
					Marine Corps Heritage Foundation.&nbsp; When completed, the 
					Museum will be the centerpiece of a campus called the Marine 
					Corps Heritage Center - a place where Marines from across 
					the country can convene together, share experiences, 
					celebrate victories, and honor their fallen.&nbsp; It will also 
					become a gateway for the American people to have the 
					opportunity to learn and understand just what it takes to be 
					a Marine and the role that Marines have played in the 
					history of our nation.&nbsp;</p>
